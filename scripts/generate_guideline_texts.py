def main():
    parser = argparse.ArgumentParser(description="生成诊疗指南知识点文本")
    parser.add_argument("--output", type=str, default="data/guidelines/raw/")
    args = parser.parse_args()

    output_dir = Path(args.output)
    output_dir.mkdir(parents=True, exist_ok=True)

    # 保存为单个 JSON 文件（build_guideline_index.py 支持此格式）
    output_path = output_dir / "clinical_guidelines.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(GUIDELINES, f, ensure_ascii=False, indent=2)

    print(f"生成 {len(GUIDELINES)} 条诊疗指南知识点")
    print(f"保存至: {output_path}")

    # 统计科室覆盖
    sources = [g["source"] for g in GUIDELINES]
    print("\n覆盖指南:")
    for i, src in enumerate(sources, 1):
        content_len = len(GUIDELINES[i-1]["content"])
        print(f"  {i:2d}. {src} ({content_len}字)")

    total_chars = sum(len(g["content"]) for g in GUIDELINES)
    print(f"\n总计: {total_chars} 字")


if __name__ == "__main__":
    main()
